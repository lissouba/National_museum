				     <h1><center>Our Museums</center></h1>
				<p>The Institute of National Museums of Rwanda and Heritage sites are a range of attractions aimed at everyone. Whether you’re looking for fun, knowledge, education or relaxation; you can rediscover or discover Rwanda’s multilayered history whilst we preserve traditional practices with traditional skills developments. </p>
				<p>By the institute looking at the past, it means that transformational changes are designed to keep stories refreshed, allowing our visitors to benefit from an excellent experience as we all build our future. National Museums and natural&cultural heritage must reflect the values of our nation and our members of staff replicate this with their creativity and energy. </p>
				<p>The time is right to remind ourselves of the need to protect and promote our cultural heritage as well as cultural tourism that our country offers. </p>
				<p>The diversity and range of museums and heritage sites gives the Institute an opportunity to present more to the global community. We have traced the history back further to World War I in collaboration with the German Embassy based in Rwanda. Steel lorry wheels along with some archived photographs were given to the Institute by Germany embassy in september 2013. The Institute prides itself on working collaboratively with multiple partners. Notably, it is about ensuring good working practices are set up with global partners and different communities to progress on initiatives that fulfill the national vision. For example, architectural consultancy for the buildings is sought from countries like South Africa,Germany United Kingdom and Belgium. </p>
				
